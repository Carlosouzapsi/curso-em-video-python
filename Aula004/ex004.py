a = input('Digite Algo: ')
print(f'Qual o tipo primitivo desse valor: {type(a)}')
print(f'Só tem um espaços? ', a.isspace())
print(f'É um numero? ', a.isspace())
print(f'Está em maiusculas?', a.isupper())
print(f'Está em minisculas?', a.islower())
print(f'Está capitalizada', a.istitle())  # capitalizado
